package com.thuss.fsa.item;

public class IncomeItem {
	private String date;
	private double income;
	
	public IncomeItem() {
		
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public double getIncome() {
		return income;
	}
	public void setIncome(double income) {
		this.income = income;
	}
	
	
}
